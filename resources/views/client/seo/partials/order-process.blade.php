    <h2 id="commercial-process-title">Як проходить замовлення</h2>
    <ol class="commercial-process__steps">
        <li><strong>Консультація</strong><span>Уточнюємо тип приміщення, вимоги та орієнтовний бюджет.</span></li>
        <li><strong>Попередній підбір</strong><span>Зіставляємо опубліковані моделі й підтверджені комплектації.</span></li>
        <li><strong>Замір</strong><span>Перевіряємо отвір, напрям відкривання та додаткові елементи.</span></li>
        <li><strong>Узгодження комплектації</strong><span>Фіксуємо модель, коробку, фурнітуру й погоджені роботи.</span></li>
        <li><strong>Доставка</strong><span>Погоджуємо адресу й передачу повного комплекту.</span></li>
        <li><strong>Монтаж</strong><span>Встановлюємо дверний блок у погодженому обсязі.</span></li>
